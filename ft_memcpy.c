#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char		*d;
	const char	*s;
	size_t		i;

	d = dest;
	s = src;
	i = -1;
	if (dest || src)
	{
		while (++i < n)
			d[i] = s[i];
		return (dest);
	}
	return (NULL);
}
